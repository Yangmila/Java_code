package Book;

public class Book 
{
	public String num;
	public String name;
	public String author;
	public String press;
	public String type;
	
	public Book(String num,String name,String author,String press,String type)
	{
		this.num = num;
		this.name = name;
		this.author = author;
		this.press = press;
		this.type = type;
	}

}
